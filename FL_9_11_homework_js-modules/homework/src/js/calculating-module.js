
export function addition(firstNumb, secondNumb) {
    return firstNumb + secondNumb;
}

export function subtraction(firstNumb, secondNumb) {
    return firstNumb - secondNumb;
}

export function multiplication(firstNumb, secondNumb) {
    return firstNumb * secondNumb;
}

export function division(firstNumb, secondNumb) {
    return firstNumb / secondNumb;
}